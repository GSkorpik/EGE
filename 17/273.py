for i in range(len(a)-2):
    if a[i]+a[i+1]+a[i+2]<am:
        k+=1
        b.add(a[i])
        b.add(a[i+1])
        b.add(a[i+2])

maxx=max(b)+min(b)

print(k,maxx)



