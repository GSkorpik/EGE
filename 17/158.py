
k=1
maxl=0
k2=0
with open('17-1.txt') as f:
    a=int(f.readline())
    for i in f.readlines():
        b=int(i)

        if a>b:
            k+=1
            if k>maxl:
                maxl=k
                k2=0
            if k==maxl:
                k2+=1
        else:
            k=1
        a=b
print(maxl,k2)

