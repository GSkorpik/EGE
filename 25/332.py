def d(n):

    for i in range(2,round(n**0.5)+1):
        if n%i==0:
            summ=i+(n//i)
            return summ


n=800000-1
k=0
while k!=5:
    s=d(n)
    if str(s)[-1]=='2':
        print(n,s)
        k+=1
    n-=1


